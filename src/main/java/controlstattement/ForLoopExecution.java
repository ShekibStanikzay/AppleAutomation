package controlstattement;

public class ForLoopExecution {

	public static void main(String[] args) {

		for ( int i=1; i<=2;i++)
			System.out.println("Afghanistan");
		System.out.println("******************************");
		for ( int y=1; y<=5;++y)
			System.out.println(y);
		System.out.println("*******************************");
		for ( int u =1; u<=4;++u)
			System.out.println(u);
		
		System.out.println("*******************************");
		
		for ( int o=6;o>=2;o--)
			System.out.println(o);

	}

}
